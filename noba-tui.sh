        --menu "Choose a script to run:" 20 50 10 \
        "Backup"      "Run backup-to-nas.sh" \
        "Verify"      "Run backup-verifier.sh" \
        "Checksum"    "Run checksum.sh" \
        "Disk"        "Run disk-sentinel.sh" \
